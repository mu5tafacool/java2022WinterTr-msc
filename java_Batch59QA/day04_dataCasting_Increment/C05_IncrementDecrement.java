package day04_dataCasting_Increment;

public class C05_IncrementDecrement {
    public static void main(String[] args) {
        int sayi1=20;

        System.out.println(sayi1+ 10);

        System.out.println(sayi1);

        sayi1=sayi1+10;
        System.out.println(sayi1);

        System.out.println(sayi1=sayi1+=10);
        System.out.println(sayi1);

    }
}
